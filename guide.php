<h2>Leveling</h2>
<p>For Leveling we would recommend doing the main quest, since the Main Quest has increased Exp and Gold. Taking the Board Quest close to a dungeon entrance, if the dungeons offers one, is also a good idea. You should also pick up Side Quests, otherwise you will have to grind dungeons at some point to continue the Main Quest. Keep in mind to clear out your Inventory, every now and then, if you don’t want to run around with a full inventory.

Around Level 15 you will receive your Job Change Quest, you should try to complete it first, cause it will speed up your level up progress.</p>

<hr class="featurette-divider" style="width: 70%;"> <!--  DIVIDER -->

<h2>Level Capped</h2>
<p>Once you are 40, you shouldn’t have that much gear, from here you have two options.
First option would be farming Abyss Dungeons to gear up a bit, this can be done alone or in a Party, a Party makes it a lot faster. Not only will you obtain rare gear pieces and rare plates, you will also obtain codes, suffix and upgrade materials like Ordinary Agates, Ordinary Alteums, Ordinary Diamonds and rare Paculta’s Crystals. Dungeons also drops some gold and most importantly Dimensional Crystal Fragments, which can be used to exchange for Skill Plates at Heraldry Scholars Stan that can be found in the North East of Saint Haven.</p>
<br>
<p>The other option would be to straight up do Nests, you can start by doing Cerberus and craft some gear from there or you just go straight for Manticore or Apocalypse Nest, which shouldn’t be too hard to clear, if you know exactly what you are doing.</p>

<hr class="featurette-divider" style="width: 70%;"> <!--  DIVIDER -->

<h2>Gearing Up</h2>
<p>There are 2 Epic sets for the 40 cap, Apocalypse and Manticore , you choose your set depending on what type of stat your class needs. You can craft those sets at the black smith. Trade > Item Production > Set Equipment </p>
<img class="center"  src="img/guide/crafting.png"></img>

