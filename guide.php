<h2>Gear Suffixes</h2>

<img class="center" style="width: 1000px; height: 800px;" src="https://i.imgur.com/5WWuoMC.png"></img>

<p>You can upgrade your gear with Suffix. Suffix applies an extra effect and a stat increase on your gear piece, depending on the suffix you choose.You can check out the effect by selecting the suffix and hovering over the weapon at the Blacksmith. To apply suffix on your gear you will need a few materials. Pacultas’s Crystal will be used on all suffixes, no matter what effect you choose. The Crystals have different rarities: Rare, Epic and Unique and those are needed to apply suffix on gear of the corresponding rarity. Rare Crystals are obtainable from Abyss level 40 Abyss Dungeons, Epic Crystals from Nests and Unique are obtainable from Sea Dragon Nest. You can upgrade Rare Pacultas’s Crystals to Epic using Octagonal Water at the Blacksmith. Besides the Crystals you will also need some other materials that drop from Dungeons. You can check what material you need and where they are obtainable at the blacksmith, by just hovering over them. Note all of the Suffix materials are also obtainable from the Chaos Openings(not released yet). 
</p>

<hr class="featurette-divider" style="width: 70%;"> <!--  DIVIDER -->

<h2>Codes and Potentials</h2>

<img class="center" style="width: 500px; height: 500px;" src="https://i.imgur.com/ES2HliA.png"></img>

<p>There are 3 different types of Codes: Agate Codes used for Armor, Crystal Codes used for Weapons and Diamond Codes used for Accessoires. Once right clicking the code, you will be able to put in a weapon of the same rarity and apply the Potential (Potentials are different depending on the rarity of the gear piece.). You can do that as much as you want, until you have the potential you want, you can also not apply the potential you rolled, if you prefer the previous role more, you can keep it, by paying a fee, however that’s not recommended, since it’s super expensive. Codes are obtainable from Abyss dungeons and can be purchased by Heraldry Scholars Stan. 
</p>

<hr class="featurette-divider" style="width: 70%;"> <!--  DIVIDER -->

<h2>Skill Accessories</h2>
<p>A Skill Accessory will increase a selected skill by 1 level, it’s important to know that once a skill reaches level 6 or 11 it gets a huge power spike.</p> 

<img class="center" style="width: 500px; height: 500px;" src="img/guide/skillaccexample.png"></img>

<p>
Skill Accessories are a craftable Gear Piece. Depending on what skill you want to level up, you will need either a Sylvie Necklace, Earrings or a Ring and one of the two Types of Skill Fragments. The Sylvie Accessoires are obtainable from Manticore and Apocalypse Hell Mode. (Not out yet.) And the Fragments are obtainable in the Chaos Openings. The Fragment Ambiguous Skills is obtainable from Chaos Opening Kamala and Fragment of Subtle Skills which is obtainable from Chaos Opening Balea(not out yet).
You should know that, it doesn’t matter which Accessory you use (e.g Vigor, Tent, Wise, etc) the stats of the Skill Accessory is completely random.
</p>

<hr class="featurette-divider" style="width: 70%;"> <!--  DIVIDER -->

<h2>Skill Plates</h2>

<img class="center" style="width: 500px; height: 500px;" src="https://i.imgur.com/mno5k8H.png"></img>

<p>Skill Plates are mainly obtainable from the Heraldry Shop in Saint Haven. Just talk to Heraldry Scholars Stan that can be found in the North East of Saint Haven. 
You can either buy the Pouches for 150 Fragments and hope you get the Plate you want or save up to buy the crafted Plate of your desire for 1k Fragments. Alternatively, you can PVP and save medals to buy the exact skill crest that you want, but they are a hefty cost of 100 medals each.
</p>

<hr class="featurette-divider" style="width: 70%;"> <!--  DIVIDER -->

<h2>Heraldry</h2>

<img class="center" style="width: 500px; height: 500px;" src="https://i.imgur.com/dRANWmx.png"></img>

<p>Heraldries can be obtained from Dungeons (Normal and (Rare in Abyss)) and Hell Nests like Cerberus Hell and Manticore, Apocalypse Hell mode later on. Normal Nests also have a chance to drop Plates, however the chance is really low.To craft the Plate you will have to talk to Heraldry Scholars Stan and have some spare Agates. 
</p>

